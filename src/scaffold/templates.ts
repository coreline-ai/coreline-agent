export function getScaffoldTemplate(kind: ScaffoldKind): ScaffoldTemplateDefinition {
  return SCAFFOLD_TEMPLATES[kind];
}

function renderScaffoldTokens(name: ScaffoldNameParts, kind: ScaffoldKind): Record<string, string> {
  return {
    input: name.input,
    normalized: name.normalized,
    kebab: name.kebab,
    camel: name.camel,
    pascal: name.pascal,
    kind,
  };
}

export function renderScaffoldFiles(
  kind: ScaffoldKind,
  name: ScaffoldNameParts,
): ScaffoldRenderedFileSpec[] {
  const template = getScaffoldTemplate(kind);
  const tokens = renderScaffoldTokens(name, kind);

  return template.files.map((file) => ({
    relativePath: renderTemplate(file.relativePathTemplate, tokens),
    absolutePath: "",
    description: file.description,
    content: renderTemplate(file.contentTemplate, tokens),
  }));
}
