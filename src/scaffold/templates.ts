import type {
  ScaffoldKind,
  ScaffoldNameParts,
  ScaffoldRenderedFileSpec,
  ScaffoldTemplateDefinition,
  ScaffoldTemplateFileSpec,
} from "./types.js";

function renderTemplate(template: string, tokens: Record<string, string>): string {
  let rendered = template;
  for (const [key, value] of Object.entries(tokens)) {
    rendered = rendered.replaceAll(`{{${key}}}`, value);
  }

  if (rendered.includes("{{")) {
    throw new Error(`Unresolved scaffold template token in "${template}"`);
  }

  return rendered;
}

function createTemplate(
  kind: ScaffoldKind,
  label: string,
  summary: string,
  files: ScaffoldTemplateFileSpec[],
  notes: string[],
): ScaffoldTemplateDefinition {
  return {
    kind,
    label,
    summary,
    files,
    notes,
  };
}

